    private void btn_abrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_abrirActionPerformed
        // TODO add your handling code here:
         int valor = jFC_fichero.showOpenDialog(this);
        if(valor == JFileChooser.APPROVE_OPTION){
            fichero = jFC_fichero.getSelectedFile();
            txt_rutaArchivo.setText(fichero.getPath());
        }

    }//GEN-LAST:event_btn_abrirActionPerformed

    private void btn_enviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_enviarActionPerformed
        // TODO add your handling code here:
         String ruta = txt_rutaArchivo.getText().trim();
        if (ruta.length() == 0){
            JOptionPane.showMessageDialog(this, "Debe seleccionar un archivo.");
            return;
        }
        String pass = txt_rutaPass.getText().trim();
        if (pass.length() == 0){
            JOptionPane.showMessageDialog(this, "Debe introducir una contrase√±a.");
            return;
        }
        cf = new Cifrado(fichero, pass);
        
        if (rbtn_cifrar.isSelected()){
            cf.Codifica();
            JOptionPane.showMessageDialog(this, "Fichero "+fichero.getPath()+" cifrado con exito.");
        }
       if (rbtn_descifrar.isSelected()){
            cf.Descodifica();
            JOptionPane.showMessageDialog(this, "Fichero "+fichero.getPath()+" descifrado con exito.");
        }
        
        
        limpiar();

    }//GEN-LAST:event_btn_enviarActionPerformed

