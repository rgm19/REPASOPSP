    private void btn_examinarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_examinarActionPerformed
        // TODO add your handling code here   
         JFileChooser buscador=new JFileChooser();
       
       buscador.setFileSelectionMode(JFileChooser.FILES_ONLY);
        //Creamos el filtro
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("*.TXT", "txt");
        //Le indicamos el filtro
        buscador.setFileFilter(filtro);
       
       //guardamos la seleccion de fichero
       int seleccion=buscador.showOpenDialog(buscador);
       
       if(seleccion==JFileChooser.APPROVE_OPTION){

	//Seleccionamos el fichero
	File fichero=buscador.getSelectedFile();

       
	//Escribe la ruta del fichero seleccionado en el campo de texto
	txt_ruta.setText(fichero.getAbsolutePath());
        
       }
    }//GEN-LAST:event_btn_examinarActionPerformed


